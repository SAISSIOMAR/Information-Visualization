def transform_data(data):
    result = {"name": "World", "children": []}

    for continent, continent_data in data.items():
        continent_entry = {
            "name": continent,
            "type": "continent",
            "children": []
        }

        for genre, genre_data in continent_data.items():
            genre_entry = {
                "name": genre,
                "type": "genre",
                "PublicationDates": [{"yearOfPublication": int(year), **attributes} for year, attributes in
                                     genre_data.items()]
            }

            continent_entry["children"].append(genre_entry)

        result["children"].append(continent_entry)

    return result


